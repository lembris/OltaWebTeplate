<script>
document.addEventListener('DOMContentLoaded', function() {
    const testimonialsSection = document.querySelector('.testimonials-section-v4');
    
    if (testimonialsSection) {
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    testimonialsSection.classList.add('in-view');
                }
            });
        }, {
            threshold: 0.2,
            rootMargin: '0px 0px -50px 0px'
        });
        
        observer.observe(testimonialsSection);
    }
    
    // Read more toggle functionality
    document.querySelectorAll('.testimonial-card-v4 .read-more').forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            const card = this.closest('.testimonial-card-v4');
            const reviewText = card.querySelector('.review-text');
            
            if (reviewText.classList.contains('expanded')) {
                reviewText.classList.remove('expanded');
                this.textContent = 'Read more';
            } else {
                reviewText.classList.add('expanded');
                this.textContent = 'Show less';
            }
        });
    });
});
</script>
